          mret(i,:)=mret(i,:)-mret(j,:)*(mret(i,j)/mret(j,j));
      end
  end
  x=zeros(1,m);
  for s=m:-1:1
      c=0;
      for k=2:m
          c=c+mret(s,k)*x(k);
      end
      x(s)=(mret(s,n)-c)/mret(s,s);
   
  end
  gret=x'
endfunction


